#----------------------------------------------------------------------------#
# Imports
#----------------------------------------------------------------------------#

from flask import Flask, request
import time
import os

#----------------------------------------------------------------------------#
# App Config.
#----------------------------------------------------------------------------#

app = Flask(__name__)

#----------------------------------------------------------------------------#
# Controllers.
#----------------------------------------------------------------------------#


@app.route('/api/time')
def get_current_time():
    return {'time': time.time()}


@app.route('/api/protein', methods=['GET', 'POST'])
def protein_query():
    if request.method == 'POST':
        return {"Method": "Post"}
    else:
        return {"Method": "Get"}

#----------------------------------------------------------------------------#
# Launch.
#----------------------------------------------------------------------------#

# Default port:
if __name__ == '__main__':
    app.run()
